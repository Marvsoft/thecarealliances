                        <h6>Our programs and mission: </h6>

                        <p>Presented by</p>
                        <p>King Streets<br>Introduction<br>CARE ALLIANCE 10 is a comprehensive and collaborative
                            effort to set aside superficial differences for the benefit of the community as a whole.
                            Our philosophy is simple, teamwork is the foundation on which all great enterprises are
                            built. In order to achieve this, all partners must come to a consensus that operates
                            under the notion that all men are created equal.</p>
                        <p>Thusly, agreeing that sharing in the effort to achieve common goals will be more
                            productive than solitary efforts. CARE ALLIANCE 10 enhances the<br>teamwork model by
                            identifying the needs of corporations and fulfilling them with the resources of the
                            community.</p>
                        <p>“He who is greatest among us shall serve.” Dr. Martin Luther King, Jr.<br>In the spirit
                            of Dr. Martin Luther King’s vision of unity and dignity, King Streets, LLC will place
                            community resource centers on ever King Street in the United States of America. These
                            centers will provide key resources to citizens and businesses alike that foster
                            corporate and private sustainability.</p>
                        <p>The vision is grand, but the concept is simple.</p>
                        <p>CARE ALLIANCE 10 will help lead corporations and people to a brighter future through
                            self-sufficient collaboration. Many people dismiss the value in areas of blight. In so
                            doing, they ultimately dismiss the fact that the greatest capital a community has to
                            invest is human capital. By investing in human capital, corporations will see their
                            greatest return and communities will experience enhanced outcomes for the lives of all
                            of its members.</p>
                        <p>Each layer of the ALLIANCE 10 project is designed to transform the common deficiencies of
                            economically disadvantaged communities into corporate solutions. Imagine projecting a
                            shortfall in IT professionals and then training a team of young IT professionals to fill
                            the void before it is ever experienced. The possibilities for precision recruiting and
                            corporate innovation are endless.</p>
                        <p>The unique technology offered through this project is a hybrid based on Google’s model
                            for data mining. The software seamlessly collects data on the needs of the community
                            while identifying opportunities to mitigate corporate losses and enhance revenue. Every
                            person that we help leads<br>to a corporate benefit, the inverse is also true.</p>
                        <p>People will flock to our physical and virtual centers for assistance, leaving valuable
                            recruiting, marketing, and planning information in their path. It enhances our ability
                            to aid the community and improve corporate outcomes.</p>
                        <p>“If a man doesn’t have a job or an income, he has neither life nor liberty or the
                            possibility for the pursuit of happiness.” Dr. Martin Luther King, Jr.<br>At the crux of
                            this effort is self-sufficiency. The idea that people have within them all the tools
                            they need for success.</p>
                        <p>The ALLIANCE 10 effort harnesses the unique thoughts, feelings, dreams, and abilities of
                            citizens and turns them into tangible assets for the benefit of the community at large.
                            The ALLIANCE 10 model consists of the following facets: Legal Aid, Nonprofit
                            Corporations and Community Development Corporations, International Resources, Historical
                            Societies, Community Think Tanks, Personal Development, Business Development, Spiritual
                            Development, Education, and Holistic Health.</p>
                        <p>Personal Outcomes<br>Before the specific dynamics of this project are discussed, I want
                            to introduce you to John. John is a teenager growing up in Chicago. He believes he has
                            no future. He studies hard, but barely achieves passing grades. He plays hard, but isn’t
                            athletic enough to be placed on any school teams. By all accounts, he will never be
                            noticed or afforded an opportunity to advance in life. There is one problem, John wants
                            more for his life. He just doesn’t know how to go about obtaining a better life. He
                            hangs out with his friends and everyone seems to love him, but that isn’t enough.</p>
                        <p>One day a locally based corporation makes an investment in ALLIANCE 10, they provide
                            computers and internet for the program. John visits the center because it is just blocks
                            from his home. He doesn’t need a car or bus fair to reach the center and this is
                            important, because his family doesn’t have the extra resources to afford either.</p>
                        <p>By participating in life mapping and educational services at his local center, John is
                            able to identify that he is a social media genius. Enlightened with this new
                            information, John starts a crowdfunding campaign and launches his very own social media
                            marketing agency. His agency earns enough profit for him to pay for college, where he
                            majors in Business Management. The entire time John remembers the corporation that aided
                            him.</p>
                        <p>Once John graduates he earns an internship at the sponsor’s corporation and is the
                            hardest working, most loyal member of the corporate team. John produces three times as
                            much revenue as his counterparts, because he is a happy employee. John’s entire family
                            knows who afforded them the opportunity for a better life and they patronize the
                            sponsor’s corporation faithfully.</p>
                        <p>John’s friends all know his meager beginnings and they also patronize the sponsor’s
                            corporation faithfully. John is a fictitious character, but his story is that of many
                            people across this nation. Those who want a better life, are willing to work for it, but
                            simply need a plan to achieve their dreams. These individuals are our neighbors, they
                            may be our friends, or they could be even closer.</p>
